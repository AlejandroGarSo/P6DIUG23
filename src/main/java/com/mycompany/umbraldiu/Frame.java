    private void mSetTresholdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mSetTresholdActionPerformed
        // TODO add your handling code here:
        if(img == null){
            JOptionPane.showMessageDialog(this, "No ha seleccionado imagen", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                int treshold = Integer.parseInt(JOptionPane.showInputDialog(this,"Introduzca un número entero"));
                imgu = Umbralizador.umbralizar(img, treshold);
                imageC.setImage((BufferedImage) HighGui.toBufferedImage(imgu));
            }catch(NumberFormatException E){
                JOptionPane.showMessageDialog(this, "Introduzca un número entero", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_mSetTresholdActionPerformed

    private void mOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mOpenActionPerformed
        filtro = new FileNameExtensionFilter("Imagen", "jpg", "png");
        fc.setFileFilter(filtro);
        int res = fc.showOpenDialog(null);
        if(res == JFileChooser.APPROVE_OPTION){
            File fichero = fc.getSelectedFile();
            img = Imgcodecs.imread(fichero.getAbsolutePath());
            imageC.setImage((BufferedImage) HighGui.toBufferedImage(img));
            pack();
        }
    }//GEN-LAST:event_mOpenActionPerformed

    private void mSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mSaveActionPerformed
        int res = fc.showSaveDialog(null);
        if(img == null){
            JOptionPane.showMessageDialog(this, "No ha seleccionado imagen", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            if(res == JFileChooser.APPROVE_OPTION){
                if(imgu == null){
                    int resu = JOptionPane.showConfirmDialog(this, "La imagen no se ha modificado. ¿Guardar de todos modos?", "IMAGEN NO MODIFICADA", JOptionPane.YES_NO_OPTION);
                    if(resu == JOptionPane.YES_OPTION){
                        File fichero = fc.getSelectedFile();
                        Imgcodecs.imwrite(fichero.getAbsolutePath(),img);
                    }
                }else{
                    int res2 = JOptionPane.showConfirmDialog(this, "¿Quiere guardar el archivo?", "GUARDAR IMAGEN", JOptionPane.YES_NO_OPTION);
                    if(res2 == JOptionPane.YES_OPTION){
                        File fichero = fc.getSelectedFile();
                        Imgcodecs.imwrite(fichero.getAbsolutePath(),imgu);
                    }
                }
            }
        }
    }//GEN-LAST:event_mSaveActionPerformed

