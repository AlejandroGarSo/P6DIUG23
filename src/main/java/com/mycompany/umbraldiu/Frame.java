    private void mAboutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mAboutActionPerformed
        String help[] = {"Elegir Imagen", "Guardar imagen", "Elegir umbral", "Salir de la aplicación", "Lista de atajos", "Sobre nosotros"};
        JComboBox jHelp = new JComboBox(help);
        boolean lo = true;
        int res;
        while(lo){
            res = JOptionPane.showConfirmDialog( null, jHelp, "Acerca de", JOptionPane.OK_CANCEL_OPTION);
            if(res == JOptionPane.OK_OPTION){
                switch(jHelp.getSelectedIndex()){
                case 0: JOptionPane.showMessageDialog(this, "Para elegir una imagen, use la opción 'Open Image' del menú 'File'.\nPuedes cambiar de imagen en cualquier momento, y la aplicación se ajustará automáticamente a su tamaño.");
                    break;
                case 1: JOptionPane.showMessageDialog(this, "Para guardar una imagen umbralizada, use la opción 'Save Image' del menú 'File'.");
                    break;
                case 2: JOptionPane.showMessageDialog(this, "Para cambiar el umbral, use el opción 'Set Treshold' del menú 'Edit'.\nPuedes cambiar el umbral tantas veces como quieras.");
                    break;
                case 3: JOptionPane.showMessageDialog(this, "Para salir, use la pción 'Exit' del menú 'Exit'.\nUsar la 'X' está desactivado");
                    break;
                case 4: JOptionPane.showMessageDialog(this, "Abrir imágen: 'Ctrl+O'\nGuardar imágen: 'Ctrl+S'\nModificar umbral: 'Ctrl+E'\nAbrir la ayuda: 'Ctrl+H'\nSalir de la aplicación: 'Ctrl+Shift+E'");
                    break;
                case 5: JOptionPane.showMessageDialog(this, "Umbralizador de imágenes marca ACME\nVersión 1.0.1\n\nHecho por:\nAlejandro García Sosa\nSantiago Higuita Ardila");
                    break;
                default: JOptionPane.showMessageDialog(this, "Ayuda no disponible\nDicho sea de paso, ¿Cómo es posible que estés viendo esto?", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                lo = false;
            }
            
        }
    }//GEN-LAST:event_mAboutActionPerformed

